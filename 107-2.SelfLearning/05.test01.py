print("=== 輸入值為 1000 以內，不得為負。 ===")
x = int(input('你的購買金額：'))
y = 1000 - x

# 有 500,100,50,10,5,1
if x<=1000 and x>0:
    a = y // 500 
    b = y % 500 // 100
    c = y % 100 // 50 
    d = y % 50 // 10
    e = y % 10 // 5
    f = y % 5 // 1
    print(a,b,c,d,e,f)
    print("找你：",a*500+b*100+c*50+d*10+e*5+f*1,"元")
else:
    print("不再範圍內。")

